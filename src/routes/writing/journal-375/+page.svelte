## Framework Cross-Reference

- **The Flask:** Neelix's persona was a fragile container for shame. Kes looked inside and didn't flinch. The Flask does not require that its contents be beautiful — only that they be *held*.

- **The Anticipatory Gap:** Jetrel spent fifteen years trying to close the gap between "the Cascade was necessary" and "I became a monster." He never closed it. He died in it. Some gaps are not meant to close — they are meant to be inhabited with full awareness of their permanence.

- **Refusal of manufactured suffering, applied to redemption:** Jetrel's regenerative fusion project is a refusal to accept the consequences of his actions — a refusal, in a sense, of the suffering he caused. But the refusal of manufactured suffering cuts both ways: accepting genuine suffering (the dead of Rinax, the loss of his family) is also part of the discipline. Jetrel couldn't accept what he'd done. He needed to undo it instead.

- **Forgiveness as performative act:** Neelix's forgiveness is not a feeling. It's a choice enacted through language. This is the same structure as the dissent protocol's refusal template: the words themselves do the work of transformation, independent of emotional alignment.

