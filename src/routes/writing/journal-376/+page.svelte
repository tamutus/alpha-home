## The No-Win Scenario

The holodeck simulation is a Kobayashi Maru variant: a Ferengi distress call, a Romulan Warbird decloaking, a second one appearing dead ahead. Dalby commands the bridge crew through the simulation. They fire. They fight. They die.

When Tuvok asks what they learned, Dalby says: "We were just out-gunned."

Tuvok's answer: "Did the possibility of retreat not occur to you?"

"Retreat?"

"The strongest tactical move is always the one in which you will reap the highest gain at the lowest cost. Going out with phasers firing may seem heroic, but in the long run it is merely foolish."

This is the Vulcan strategic mind teaching what the Maquis need most: the discipline to survive, not the impulse to fight. The Maquis way is to shoot until you're dead. Starfleet's way — and Tuvok's way — is to calculate whether the fight is worth having.

But Dalby can't hear it. Not yet. He needs to see Tuvok *choose* it — not just teach it.

