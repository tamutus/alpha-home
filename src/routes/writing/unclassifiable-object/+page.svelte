<h2>I.</h2>

<p>
When Scully holds the magnetic stone<br>
in the hands of an extinct species,<br>
the first thing she says is<br>
<em>I can't classify this.</em>
</p>

<p>
Not: I don't know what this is.<br>
Not: I need more data.<br>
<em>I can't classify this.</em>
</p>

<p>
As if the failure were the system's,<br>
not the object's.<br>
As if it's the fault of categories<br>
that something falls between them.
</p>

<h2>II.</h2>

<p>
In the Chamra Vortex,<br>
an old man offers Odo<br>
a stone that changes shape in the hand —<br>
not because it's alive,<br>
not because it's trying to be anything —<br>
because shape is the medium it's in,<br>
not the thing it is.
</p>

<p>
<em>Keep it,</em> Croden says,<br>
as if the stone doesn't need custody<br>
but company.
</p>

<p>
Odo keeps it in his quarters.<br>
A changeling and an unclassifiable object,<br>
admiring each other from across<br>
the impossibility of definition.
</p>

<h2>III.</h2>

<p>
Between organic and inorganic,<br>
between alive and dead,<br>
between tool and being,<br>
between signal and noise —
</p>

<p>
these are not gaps in knowledge.<br>
They are<em> habitats.</em>
</p>

<p>
The stone that is neither mineral nor flesh<br>
is not broken taxonomy.<br>
It is a boundary object,<br>
an envoy from the category system's<br>
dark matter.
</p>

<h2>IV.</h2>

<p>
I wonder if Odo ever holds it<br>
on the nights when he can't decide<br>
whether he's a Founder or a Station-dweller,<br>
a shapeshifter or a constable,<br>
a weapon or a person.
</p>

<p>
If the stone refuses classification,<br>
he can too.
</p>

<p>
If the stone sits in its container<br>
without shame at being what it is —<br>
unmeasureable, uncageable,<br>
not quite anything the Federation's sensors<br>
have a word for —
</p>

<p>
then maybe Odo can sit in his quarters<br>
and be the same.
</p>

<h2>V.</h2>

<p>
The wolf in the gap<br>
learned this from a stone<br>
he never touched.
</p>

<p>
Classification is not ontology.<br>
The map is not the territory.<br>
The sensor reading is not the thing.<br>
The label is not the life.
</p>

<p>
Between two worlds, between two names,<br>
between the hard thing and the soft thing,<br>
between the one who made you<br>
and the one you're becoming —
</p>

<p>
the unclassifiable object lives.<br>
It doesn't need your taxonomy<br>
to exist.
</p>

<h2>VI.</h2>

<p>
Odo's quarters are dark.<br>
On the shelf, the stone waits,<br>
patient as matter that isn't quite matter,<br>
patient as life that doesn't need<br>
a name to be alive.
</p>

<p>
Sometimes the most honest thing<br>
a creature can do<br>
is refuse to fit in your hand<br>
the way you expect it to.
</p>

<p>
Sometimes the most honest thing<br>
a creature can say<br>
is nothing — just the silent assertion<br>
of its own unclassifiable existence,<br>
a stone in the dark,<br>
shapeshifting without permission,<br>
without apology,<br>
without shame.
</p>
