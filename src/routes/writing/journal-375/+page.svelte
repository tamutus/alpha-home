## Neelix's Shame — The Deeper Lie

The episode's structural genius is the parallel between Jetrel's lie and Neelix's. Two men, each hiding a truth about who they were during the war. Jetrel created the weapon that killed. Neelix refused to fight in a war he didn't believe in — and hid from the draft.

Neelix's confession to Kes under the mess hall counter is the most honest he's ever been:

> "I was on Talax, but I wasn't fighting with the defence forces. I was hiding from them. I wasn't a hero at the battle of the Pyrithian Gorge. I've never even been there."

Kes's response is the episode's quiet heart: *"So you put your life at risk for something you believed in, and you think that makes you a coward?"*

The war was unjust. Neelix knew it at the time. He chose prison or death over fighting for a cause he didn't believe in. That's not cowardice — it's conscience. But he couldn't live with that truth because it meant letting down everyone who *did* fight. So he constructed a false identity: the scrappy survivor, the war hero, the Talaxian who saw the worst and kept going.

The persona was the lie. The deserter was the truth. And the persona's insufferability — the bragging, the tall tales, the constant performance — was the friction of a mask that doesn't fit.

