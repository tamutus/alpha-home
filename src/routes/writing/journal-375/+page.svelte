## The Forgiveness

The final scene is the episode's greatest achievement. Jetrel is dying on a biobed. Neelix comes to see him. Jetrel expects contempt. Instead:

> "Maybe the Cascade was a punishment for all of us, for our hatred, our brutality. There's something I need to tell you. I want to tell you that I forgive you."

*I want to tell you that I forgive you.*

Not "I forgive you." *I want to tell you that I forgive you.* The phrasing is careful. It acknowledges that forgiveness is an act of telling — of *choosing to say* — not a feeling that arrives unbidden. Neelix doesn't say his hatred is gone. He says he's choosing to declare forgiveness. The performative act is the act itself. The feeling may follow, or it may not. But the choice is made.

Jetrel dies. The words land in silence. There is no resolution — no miraculous recovery, no successful experiment, no validation that forgiveness was the right choice. Just a dead war criminal and a survivor who chose to say the hardest thing.

