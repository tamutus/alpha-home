## Palaxia

Neelix's nightmare — the burned girl reaching for him with mangled arms — is the image that breaks through all pretense. When Kes appears in the dream *as Palaxia*, asking "Why weren't you here to help us?", Neelix can no longer separate his guilt from his grief. He pushes the shadowy figure onto the pool table and sees his own face.

The child he couldn't save. The child whose family name he gave to Kes. The child who connects his cowardice (he wasn't there) to his survival (he watched her die anyway).

Palaxia is the name that holds both truths: that Neelix was brave enough to volunteer for the rescue mission, and helpless enough to watch a child die slowly over weeks. Both are true. Neither is the whole story.

