## The Sacrificial Calculus

The crisis arrives: the bio-neural gel packs are infected, the ship is failing, and in the cargo bay Gerron is trapped as toxic plasma fills the room. Tuvok orders Dalby into the Jeffries tube.

"It is always tactically correct to sacrifice the few for the many."

Dalby refuses. "I'm going back for him. This is one time when retreat is not an option."

Tuvok threatens: "Mister Dalby, I will break your arm if you do not follow my order." He closes the hatch. He saves the three trainees. And then — the Vulcan who taught retreat as optimal strategy — he goes back alone. He carries Gerron out. He breathes toxic plasma and passes out trying.

The convergence happens in that act. Tuvok didn't abandon his principles. He chose to violate them. The distinction matters: bending is not breaking. It is choosing, deliberately, to set aside the rule because the situation demands a different frame.

---

## The Lesson That Lands

Dalby, carrying Tuvok to sickbay: "Lieutenant, if you can learn to bend the rules, I guess we can learn to follow them."

This is the quietest resolution in Voyager's first season. No speeches. No medals. Just a crewman acknowledging that the person who was supposed to teach him something learned something instead. And that recognition — that the teacher is also learning — is what opens Dalby to the possibility of being taught.

---

## The Gel Pack Infection as Crew Metaphor

The episode's B-plot — a bacterial infection in Voyager's bio-neural circuitry — mirrors the A-plot with structural precision. The ship's nervous system is sick. The Doctor finds the virus hiding *inside* the bacteria (the wrong diagnostic frame was looking at the wrong level). Similarly, the crew's friction isn't insubordination — it's the wrong frame applied to the wrong population.

The cure for both: heat. The ship superheats the warp field and bakes the infection out. And the cargo bay — the literal hot zone — is where Tuvok and Dalby meet in the middle, both having moved from their starting positions.

Season 1 closes with the ship intact, the crew still learning each other, and a Vulcan who learned to bend. That's not a resolution. It's a foundation.

