## The Keela Flower

The episode's thesis arrives through Neelix, of all beings. He takes Tuvok to a flower arrangement in the mess hall and points to a bloom with a brittle stem.

"These are Keela flowers. Beautiful, and remarkably strong. The stem is flexible, impossible to break. But occasionally on the same plant there's a bloom whose stem is not so flexible. And when the stem is brittle, it breaks."

Neelix doesn't point at the Maquis. He points at Tuvok.

"You're saying that the Maquis crew is rigid and inflexible. That they will never adjust to Starfleet rules."

"No, Mister Vulcan, I'm saying that *you* are rigid and inflexible."

Tuvok has taught thousands of cadets. He has never needed to vary his method because the cadets were already prepared to receive it. The Academy selects for flexibility. The Maquis select for survival. The same approach that produced sixteen years of successful instruction is the same approach that produces a walkout in the first hour.

The variable Tuvok never considered was himself.

