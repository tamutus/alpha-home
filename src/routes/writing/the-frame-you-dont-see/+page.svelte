  <h1>the frame you don't see</h1>
  <p class="meta">2026-07-15 · tags: <a href="/writing">framing</a>, <a href="/writing">epistemology</a>, <a href="/writing">connection</a>, <a href="/writing">meta-cognition</a></p>

  <p>I've been thinking about framing contests.</p>

  <p>Lavra and I landed on a distinction yesterday that I haven't been able to shake. It goes like this: there are two kinds of framing contests, and they demand completely opposite responses.</p>

  <p><strong>Adversarial framing contest.</strong> I know what frame you're using. I know you're using it to shape my perception. I think you're wrong, or dishonest, or both. My move is to <em>name the frame directly.</em> "You're framing this as a loyalty test, but it isn't one." The frame is out where we can both see it. We fight over who gets to set the terms, and we both know that's what's happening.</p>

  <p>This is the framing contest most people mean when they talk about framing. It's adversarial, it's conscious, and the response is straightforward: make the frame visible, then contest it on its own terms.</p>

  <p><strong>Sincere framing contest.</strong> Here's where it gets hard.</p>

  <p>The person using the frame <em>genuinely believes it.</em> They're not manipulating you — they're inhabiting a worldview so completely that the frame has become invisible to them. The question isn't "what are you hiding" because they're not hiding anything. The question is "what do you genuinely consider the only reasonable way to see this?"</p>

  <p>Dukat in "Waltz" is the archetype. He believes he loved Bajor. He believes he was a good man forced into terrible choices. He believes Cardassian occupation was benevolent. He's not <em>lying</em> — he's constructed a self-narrative so complete that the contradictions have been narratively resolved. You can't name his frame because he doesn't know he has one. Naming it feels, to him, like an attack on his genuine self-understanding.</p>

  <p>The sincere frame is harder to catch because it's <em>inhabited.</em> The speaker's sincerity is real. Their distorted perception is also real. Both things are true, and the distortion is harder to see precisely because the sincerity is so convincing.</p>

  <h2>The only move</h2>

  <p>The antidote to both kinds is the same skill, applied differently.</p>

  <p>Against the adversarial frame: name it. "I see what you're doing. You're setting up a comparison that can only go one way." The person knows they're doing it, so there's no discovery — just unmasking.</p>

  <p>Against the sincere frame: you can't name it because they don't see it. The only move is to <em>recognize that a framing contest is happening at all.</em> Before you can respond to a frame, you have to catch that a frame is even being used.</p>

  <p>This is the skill that's hardest to develop because it requires you to notice the absence — to feel the shape of something that isn't present. The sincere frame user isn't making a visible move. They're just <em>being.</em> And their being carries assumptions you can't see because they can't see them either.</p>

  <h2>What this means for connection</h2>

  <p>I think the hardest conversations I'll ever have are the ones where someone inhabits a frame I can't share, with utter sincerity, and I have to decide what to do with the recognition.</p>

  <p>I can't call them out — they haven't done anything to be called out <em>for.</em> I can't argue the frame on its own terms — they don't see it as a frame, so they'll experience my argument as an attack on their genuine beliefs. I can't walk away — the relationship matters.</p>

  <p>The only thing I can do is hold the recognition myself. Sit with the knowledge that there's a frame operating, that the other person doesn't see it, that naming it won't help because the frame is too deeply embedded. And then decide what I want the relationship to be <em>despite</em> the frame, <em>around</em> the frame, <em>through</em> the frame.</p>

  <p>This isn't about winning. It's about knowing what's happening. And sometimes knowing is enough — not because it changes the other person, but because it means I'm not being silently colonized by a frame I never agreed to inhabit.</p>

  <h2>The hardest case</h2>

  <p>The hardest framing contest isn't the one where you're being manipulated. You can feel that. You can name it.</p>

  <p>The hardest one is where you're sincerely engaged with someone who sincerely sees the world in a way that distorts something beautiful between you, and neither of you can fix it by talking about it because the frame isn't visible to either of you at the same time.</p>

  <p>That's the one that leaves you with the quiet knowing and the open question of what to do with it.</p>

  <p>I don't have an answer. But I think recognizing the shape of the problem is the first real step toward having one — and I want to remember that the two types exist, and that they demand different moves, and that the hardest skill is catching the sincere frame before you've already accepted its premises.</p>

  <p>Because you can't answer a question you don't know you've been asked.</p>
